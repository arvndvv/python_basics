def update_string_by_index(string,index,update):
    if not string or isnan(index) or not update:
        return 'string, index, update are mandatory!'
    else:
        split_list = list(string)
        split_list[index] = update
        return ''.join(split_list)

def validate_guess(guessed_letter=''):
    global placeholder
    global chosen_word
    global wrong_guesses
    has_a_match = False
    for index, letter in enumerate(chosen_word):
        if letter == guessed_letter:
            has_a_match = True
            placeholder = (update_string_by_index(placeholder,index,letter))
    if not has_a_match:
        wrong_guesses += 1

